/** Lightweight i18n — no external deps. Fallback: English. */

export type LangId = "en" | "es" | "fr" | "pt" | "de" | "it" | "zh" | "ja";

export type Dict = Record<string, string>;

export const LANGUAGES: {
  id: LangId;
  native: string;
  english: string;
  /** BCP-47 for speechSynthesis / html lang */
  bcp47: string;
}[] = [
  { id: "en", native: "English", english: "English", bcp47: "en" },
  { id: "es", native: "Español", english: "Spanish", bcp47: "es" },
  { id: "fr", native: "Français", english: "French", bcp47: "fr" },
  { id: "pt", native: "Português", english: "Portuguese", bcp47: "pt" },
  { id: "de", native: "Deutsch", english: "German", bcp47: "de" },
  { id: "it", native: "Italiano", english: "Italian", bcp47: "it" },
  { id: "zh", native: "中文", english: "Chinese", bcp47: "zh-CN" },
  { id: "ja", native: "日本語", english: "Japanese", bcp47: "ja" },
];

export const DEFAULT_LANG: LangId = "en";

export function isLangId(v: unknown): v is LangId {
  return typeof v === "string" && LANGUAGES.some((l) => l.id === v);
}

export function langMeta(id: LangId) {
  return LANGUAGES.find((l) => l.id === id) || LANGUAGES[0]!;
}

function fill(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, k: string) =>
    vars[k] != null ? String(vars[k]) : `{${k}}`,
  );
}

