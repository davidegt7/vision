export function t(
  lang: LangId | string | undefined,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const id: LangId = isLangId(lang) ? lang : DEFAULT_LANG;
  const raw = DICTS[id][key] ?? DICTS.en[key] ?? key;
  return fill(raw, vars);
}

export function languageNameForPrompt(lang: LangId | string | undefined): string {
  const id: LangId = isLangId(lang) ? lang : DEFAULT_LANG;
  return langMeta(id).english;
}

